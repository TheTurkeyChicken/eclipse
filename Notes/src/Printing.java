
public class Printing {
	
	public static void main(String[] args) {
		// this is a comment which does not affect notes
		/* this is a 
		 * long comment
		 */
		System.out.println("Hello World!");
		
		// print vs println
		// println adds an extra line after your output
		System.out.print("Justin");
		System.out.println("Adam");
		
		// escape characters allows us to print special characters
		System.out.println("Behold, a quotation mark \"");
		
		// print can be used for arithmetic expressions
		
		System.out.println(1.0/2.0);
		
		// combining text and numbers
		
		System.out.println("1 + 1 is " + (1 + 1));
		System.out.println("7 x 8 = " + 7 * 8);
		System.out.println(1 + 1 + " is two.");
	}
}
